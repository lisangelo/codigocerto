    /**
     *Adiciona painel
     *@param string com titulo do painel
     *@param JPanel painel para a area de abas
     */
    public void addPainel(String titulo, JPanel painel) {
        jTabbedPanePrincipal.addTab(titulo, painel);
        if ( ! jTabbedPanePrincipal.isVisible()) {
            jTabbedPanePrincipal.setVisible(true);
        }
        validate();
    }
    
    /**
     *Muda foco para controle do campo codigo
     */
    public void focarCodigo() {
        ccTextFormatadoCodigo.requestFocusInWindow();
    }
    
    /**
     *Insere conteudo no campo de id (codigo)
     *@param long com codigo
     */
    public void setId(long id) {
       ConversorTipos conversor = new ConversorTipos();
       conversor.setValorBase(id); 
       ccTextFormatadoCodigo.setText(conversor.getString()); 
    }
    
    /**
     *Informa conteudo do campo id (codigo)
     *@return long com codigo
     */
    public long getId() {
        long codigo = 0L;
        
        String textoId = ccTextFormatadoCodigo.getText();
        if ( ! textoId.equals("")) {
            ConversorTipos conversor = new ConversorTipos();
            conversor.setValorBase(textoId);
            codigo = conversor.getLong();
        }
        
        return codigo;
    }

    /**
     *Insere conteudo no campo de nome
     *@param string com nome
     */
    public void setNome(String nome) {
        if (nome != null) {
            ccTextFormatadoNome.setText(nome); 
        }
    }

    /**
     *Configura estado de alteracao
     *@param boolean com estado
     */
    public void setAlterado(boolean alterado) {
        ccTextFormatadoCodigo.setEnabled( ! alterado);
    }
    
    /**
     *Configura estado de Novo cadastro
     *@param boolean com estado
     */
    public void setNovo(boolean novo) {
        ccTextFormatadoCodigo.setEnabled( ! novo);
        ccTextFormatadoNome.requestFocusInWindow();
    }
    
    /**
     * Configura painel de entrada que invocou este painel de cadastro
     * @param painel 
     */
    public void setEntrada(PanelEntradaCadastro painel) {
        _panelEntrada = painel;
    }
    
    private void configurar() {
        jTabbedPanePrincipal.setVisible(false);
        ccTextFormatadoCodigo.setMascaraEdicao(MASCARA_CODIGO);
        StringBuilder mascaraNome = new StringBuilder(TAMANHO_CAMPO_NOME);
        for (int i = 0; i < TAMANHO_CAMPO_NOME; i++) {
            mascaraNome.append('*');
        }
        ccTextFormatadoNome.setMascaraEdicao(mascaraNome.toString());
        ccTextFormatadoNome.setMaiusculas(true);
    }
    
