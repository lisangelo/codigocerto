    private void ccTextFormatadoCodigoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ccTextFormatadoCodigoKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_F3) {
            UICadastro cadastro = (UICadastro) _panelEntrada.getOrigem();
            cadastro.efetuarPesquisaCadastro();
        }
    }//GEN-LAST:event_ccTextFormatadoCodigoKeyReleased

    private void ccTextFormatadoCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ccTextFormatadoCodigoFocusLost
        if (ccTextFormatadoCodigo.isAlterado()) {
            UIEntradaDados cadastro = _panelEntrada.getOrigem();
            long id = getId();
            if ( ! cadastro.isCadastrado(id)) {
                _panelEntrada.setNovo(true);
                setId(id);
            } else {
                _panelEntrada.setAlterado(false);
                cadastro.exibirDados();
                ccTextFormatadoNome.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_ccTextFormatadoCodigoFocusLost
    
    
